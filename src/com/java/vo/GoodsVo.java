package com.java.vo;

public class GoodsVo {

	private String goods_id;	//商品编号
	private String goods_name;	//商品名称
	private int goods_num;		//商品数量
	private int goods_prices;	//商品价格
	private String goods_type;		//商品类型
	private String goods_unit;		//商品单位
	private String warehouse_id;	//商品所在仓库
	
	private String typeVo;
	private String warehouseVo;
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public int getGoods_num() {
		return goods_num;
	}
	public void setGoods_num(int goods_num) {
		this.goods_num = goods_num;
	}
	public int getGoods_prices() {
		return goods_prices;
	}
	public void setGoods_prices(int goods_prices) {
		this.goods_prices = goods_prices;
	}
	public String getGoods_type() {
		return goods_type;
	}
	public void setGoods_type(String goods_type) {
		this.goods_type = goods_type;
	}
	public String getGoods_unit() {
		return goods_unit;
	}
	public void setGoods_unit(String goods_unit) {
		this.goods_unit = goods_unit;
	}
	public String getWarehouse_id() {
		return warehouse_id;
	}
	public void setWarehouse_id(String warehouse_id) {
		this.warehouse_id = warehouse_id;
	}
	public String getTypeVo() {
		return typeVo;
	}
	public void setTypeVo(String typeVo) {
		this.typeVo = typeVo;
	}
	public String getWarehouseVo() {
		return warehouseVo;
	}
	public void setWarehouseVo(String warehouseVo) {
		this.warehouseVo = warehouseVo;
	}
	
	
	
	
}
